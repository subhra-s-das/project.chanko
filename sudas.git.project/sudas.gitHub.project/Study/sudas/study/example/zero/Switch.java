package sudas.study.example.zero;

public class Switch {

	public static void main(String[] args) {

// switch example
		
		int number=50;
		switch (number)
		{
		case 10:
			System.out.println("This is ten");
			break;
		case 20:
			System.out.println("This is twenty");
			break;
		case 30:
			System.out.println("This is thirty");
			break;
			default:
				System.out.println("this is default case");
				break;
		}
		
	}

}
