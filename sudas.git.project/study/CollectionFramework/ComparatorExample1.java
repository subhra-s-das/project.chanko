package CollectionFramework;

// example to demonstrate the use of comparator interface
// comparator is used to compare to compare two objects

class Animals {
	String name;
	String size;

	public Animals(String name, String size) {
		this.name = name;
		this.size = size;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getSize() {
		return size;
	}

	public void setSize(String size) {
		this.size = size;
	}
}

